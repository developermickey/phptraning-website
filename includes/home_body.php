    <td align="justify"><h1>Welcome to NLUO</h1>
      <div style="text-align:justify;margin-top:16px;"> &nbsp; The National Law University, Odisha (NLUO) promises to be an exciting new development in the field of legal education. A very new institution, its establishing act of legislature was notified as recently as April 2009, entrance examinations were conducted in May, and classes began in July. Already it has made its presence felt both in terms of educational quality as well as in co-curricular activities such as moot courts.</div>
      <div style="text-align:justify;margin-bottom:20px;margin-top:10px;"> &nbsp; NLUO is located in Cuttack, a city dating back to over a thousand years, and also boasting a rich legal heritage, not only is the Odisha High Court situated here, but also some of the finest lawyers and judges of the country trace their roots back to this city. The University is presently situated in temporary premises along the Mahanadi river, towards the north-west of the city's present limits. It is scheduled to move to a new purpose-built campus at Naraj, a location being developed as an institutional area. <a href="#" class="read">Read More...</a></div></td>
